#include<iostream>
using namespace std;
int main(){
    int a=2 , b=5;
    cout<<"arithmatic operator"<<endl;
    cout<<"The value of a + b is: "<<a+b<<endl;
    cout<<"The value of a - b is: "<<a-b<<endl; 
    cout<<"The value of a * b is: "<<a*b<<endl;
    cout<<"The value of a / b is: "<<a/b<<endl;
    cout<<"The value of a % b is: "<<a%b<<endl;
    cout<<"The value of a ++  is: "<<a++<<endl;
    cout<<"The value of a --  is: "<<a--<<endl;
    cout<<"The value of ++ a  is: "<<++a<<endl;
    cout<<"The value of -- a  is: "<<--a<<endl;
    return 0;
}